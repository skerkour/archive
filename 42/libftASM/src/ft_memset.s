global _ft_memset
section .text

_ft_memset:
	mov r8, rdi
	mov rax, rsi
	test rdi, rdi
	je .end
	test rdx, rdx
	je .end
	mov rcx, rdx
	rep stosb

.end:
	mov rax, r8
	ret
