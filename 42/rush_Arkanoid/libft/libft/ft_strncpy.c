#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n)
	{
		if (*src)
			dst[i++] = *src++;
		else
			dst[i++] = '\0';
	}
	return (dst);
}
